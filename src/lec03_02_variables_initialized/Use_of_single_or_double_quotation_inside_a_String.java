package lec03_02_variables_initialized;

public class Use_of_single_or_double_quotation_inside_a_String {

	public static void main(String[] args) {
		
		System.out.println("Janet Yellen, Born in 1946, American economist and Chair of the US Federal Reserve, born in Brooklyn, New York");
		System.out.println("\"Janet Yellen\", Born in 1946, " + "\n\tAmerican economist and Chair of the US Federal Reserve," + "\n\tborn in \'Brooklyn\', \'New York\'");
		
		

	}

}
