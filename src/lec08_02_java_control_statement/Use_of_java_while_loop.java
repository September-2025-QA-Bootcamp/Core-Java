package lec08_02_java_control_statement;

public class Use_of_java_while_loop {

	public static void main(String[] args) {
		
		System.out.println("----------for loop----------");
		for(int i = 0; i<5; i++) {
			System.out.println(i);
		}
		
		System.out.println("----------while loop----------");
		int i = 0;
		while(i<5) {
			System.out.println(i);
			i++;
		}

		
	}

}
